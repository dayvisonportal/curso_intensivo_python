# 5.6 Estágios da vida
age = 28

if age < 2:
    print("Você é um bebê.")
elif age < 4:
    print("Você é uma criança.")
elif age < 13:
    print("Você um(a) garoto(a).")
elif age < 20:
    print("Você é um adolescente.")
elif age < 65:
    print("Você é um adulto.")
else:
    print("Você é um(a) idoso(a)")
